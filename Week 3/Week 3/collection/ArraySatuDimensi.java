package collection;

public class ArraySatuDimensi {
    public static void main(String[] args) {
        int[] umur = {17, 22, 19, 20};

        String nama[] = new String[4];

        nama[0] = "Ali";
        nama[1] = "Budi";
        nama[2] = "Carli";
        nama[3] = "Dendi";

        System.out.println(nama[0] + " berumur " + umur[0] + " tahun");
        System.out.println(nama[2] + " berumur " + umur[2] + " tahun");
    }
}
